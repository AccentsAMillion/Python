# How to take a list and slice with negative integers to get the item needed
tempuratures = [110, 88.7, 50]

print(tempuratures[-1])

print(tempuratures[-2])

print(tempuratures[-3])

print(tempuratures[-1:-5])

print(tempuratures[:-2])

print(tempuratures[-1:])

print(tempuratures[-3:])

print(tempuratures[-4:-2])

print(tempuratures[-5:-3])











