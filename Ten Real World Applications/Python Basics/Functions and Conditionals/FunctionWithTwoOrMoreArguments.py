# len argument can take only 1 single argument
# instance argumnet can use 2 or more arguments

# Single Function Argument
def area(d):
    return d * 2

print(area(5))

# Function with more than 2 or more arguments
# To get area use the times symbol *
def area(a, b):
    return a * b

print(area(4, 20))